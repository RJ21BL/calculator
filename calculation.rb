class Calculation
  def add num_1, num_2
    puts "The answer is #{num_1 + num_2}!"
  end

  def subtract num_1, num_2
    puts "The answer is #{num_1 - num_2}!"
  end

  def times num_1, num_2
    puts "The answer is #{num_1 * num_2}!"
  end

  def divide num_1, num_2
    puts "The answer is #{num_1 / num_2}!"
  end
end